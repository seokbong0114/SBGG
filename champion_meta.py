    p = dict(DEFAULT_PROFILE)
    if "Mage" in tags:
        p["dmg"] = "AP"
    elif "Marksman" in tags:
        p["dmg"] = "AD"
    elif "Tank" in tags:
        p["dmg"] = "AP"; p["tank"] = True; p["cc"] = True
    elif "Support" in tags:
        p["dmg"] = "AP"; p["cc"] = True
    else:  # Fighter, Assassin 등
        p["dmg"] = "AD"
    return p

